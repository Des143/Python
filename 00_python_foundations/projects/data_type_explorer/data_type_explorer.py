# Data Type Explorer

# Create variables of different types
integer_num = 42
float_num = 3.14
text_str = "Python Foundations"
is_learning = True
my_list = [1, 2, 3, 4, 5]
my_tuple = (10, 20, 30)
my_dict = {"name": "Learner", "age": 25}

# Print types and values
print("Integer:", integer_num, "Type:", type(integer_num))
print("Float:", float_num, "Type:", type(float_num))
print("String:", text_str, "Type:", type(text_str))
print("Boolean:", is_learning, "Type:", type(is_learning))
print("List:", my_list, "Type:", type(my_list))
print("Tuple:", my_tuple, "Type:", type(my_tuple))
print("Dictionary:", my_dict, "Type:", type(my_dict))

# Demonstrate type conversion
str_num = str(integer_num)
print("\nType Conversion:")
print("Integer to String:", str_num, "Type:", type(str_num))
