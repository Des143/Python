# Variables in Python

# Basic variable assignment
x = 10
y = 20

# Multiple assignment
a, b, c = 1, 2, 3

# Variable reassignment
name = "Python"
print("Initial name:", name)
name = "Programming"
print("Updated name:", name)

# Type flexibility
flexible_var = 42
print("Initial type:", type(flexible_var))
flexible_var = "Now a string"
print("New type:", type(flexible_var))
