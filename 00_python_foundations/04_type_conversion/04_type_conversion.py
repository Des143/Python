# Type Conversion in Python

# String to Integer
num_str = "123"
num_int = int(num_str)
print("String to Integer:", num_int)

# Integer to String
num = 456
str_num = str(num)
print("Integer to String:", str_num)

# Float to Integer
float_num = 3.14
int_num = int(float_num)
print("Float to Integer:", int_num)

# String to Float
float_str = "3.14"
converted_float = float(float_str)
print("String to Float:", converted_float)
